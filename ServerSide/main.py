from core.core import start  # Import the start function from the core module

def main():
    """
    Main function that initiates the application.

    This function calls the start function to begin the execution of the application.
    """
    start()  # Start the application

if __name__ == "__main__":
    """
    Entry point of the script.

    This block checks if the script is being run directly (as opposed to being imported as a module).
    If so, it calls the main function to execute the application.
    """
    main()  # Execute the main function
